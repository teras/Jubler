    public void setSubtitle(Subtitles subs, int index) {
        this.subs = subs;
        SplitS.setValue(100 * index / subs.size());
        split.setTime(subs.elementAt(index).getStartTime());
    }

    public Time getTime() {
        return split.getTime();
    }

